package br.enums;

public enum TipoGabinete {
    FULL_TOWER,
    MID_TOWER,
    MINI_TOWER,
    MINI_ITX,
    MICRO_ATX,
    CUBE,
    HTPC,
    OPEN_FRAME,
    SERVER
}

/*
EXPLICAÇÃO DOS TIPOS
FULL_TOWER | Maior tipo de gabinete, ideal para setups com muitas placas e watercooling.
MID_TOWER | Mais comum; equilíbrio entre tamanho e espaço interno.
MINI_TOWER | Compacto, para quem quer um PC menor com placa Micro-ATX.
MINI_ITX | Super compacto, compatível com placas Mini-ITX. Ótimo para builds minimalistas.
MICRO_ATX | Projetado para placas Micro-ATX; menor que Mid Tower, maior que Mini-ITX.
CUBE | Formato cúbico, geralmente com duas câmaras internas.
HTPC | Gabinetes horizontais, usados para media centers (Home Theater PC).
OPEN_FRAME | Estrutura aberta, estilo "esqueleto"; geralmente usado para overclocking e testes.
SERVER | Para racks ou servidores torre; pensado pra uso corporativo.
 */